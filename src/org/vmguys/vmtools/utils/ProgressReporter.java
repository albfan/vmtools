package org.vmguys.vmtools.utils;


/**
 * A class that implements this interface can be used to report the
 * progress of a running computation.
 */
public interface ProgressReporter {

	/**
	 * Shows the progress of the computation. The method will be called
	 * from time to time with a value between 0 and 100, inclusive,
	 * indicating the percentage of the operation that has been completed. In
	 * some cases it may be called more than once with the same value,
	 * so the implementing method should be prepared to handle this
	 * (probably by filtering out the duplicate values).
	 */
	void showProgress(int itemsProcessed, int totalItems);
	
}


